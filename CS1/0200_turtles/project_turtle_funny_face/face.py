import turtle

#Create and name a variable for the turtle
pen = turtle.Turtle()
pen.shape("turtle")

#All our circles will be this size
size = 50

#Draw the right eye
pen.penup()
pen.color("blue")
pen.fillcolor("blue")
pen.goto(200,70)
pen.begin_fill()
pen.pendown()
pen.circle(size)
pen.penup()
pen.end_fill()

#Draw the left eye
pen.penup()
pen.color("blue")
pen.fillcolor("blue")
pen.goto(0,70)
pen.begin_fill()
pen.pendown()
pen.circle(size)
pen.penup()
pen.end_fill()

#Draw the smile
